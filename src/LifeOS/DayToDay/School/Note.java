package LifeOS.DayToDay.School;

import java.util.ArrayList;

public class Note {

    private String title;
    private String note;

    public Note{
        this.title = title;
        this.note = note;
    }

    public Note() {
        this.title = title;
    }

    public Note() {
        this.note = note;
    }

    public void setTitle() {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getNote() {
        return note;
    }

}
